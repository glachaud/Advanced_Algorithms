public class AnimationVariables {
  private int minSizeInput;
  private int maxSizeInput;
  private int step;
  private int minValueInput;
  private int maxValueInput;

  public int getMinSizeInput() {
    return minSizeInput;
  }

  public void setMinSizeInput(int minSizeInput) {
    this.minSizeInput = minSizeInput;
  }

  public int getMaxSizeInput() {
    return maxSizeInput;
  }

  public void setMaxSizeInput(int maxSizeInput) {
    this.maxSizeInput = maxSizeInput;
  }

  public int getStep() {
    return step;
  }

  public void setStep(int step) {
    this.step = step;
  }

  public int getMinValueInput() {
    return minValueInput;
  }

  public void setMinValueInput(int minValueInput) {
    this.minValueInput = minValueInput;
  }

  public int getMaxValueInput() {
    return maxValueInput;
  }

  public void setMaxValueInput(int maxValueInput) {
    this.maxValueInput = maxValueInput;
  }
}
